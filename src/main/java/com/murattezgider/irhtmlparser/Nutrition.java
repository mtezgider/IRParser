package com.murattezgider.irhtmlparser;

/**
 *
 * @author murattezgider@gmail.com
 */
public class Nutrition {
    private String calorie;
    private String fat;
    private String carbs;
    private String protein;
    private String cholesterol;
    private String sodium;
    

    /**
     * @return the calorie
     */
    public String getCalorie() {
        return calorie;
    }

    /**
     * @param calorie the calorie to set
     */
    public void setCalorie(String calorie) {
        this.calorie = calorie;
    }

    /**
     * @return the fat
     */
    public String getFat() {
        return fat;
    }

    /**
     * @param fat the fat to set
     */
    public void setFat(String fat) {
        this.fat = fat;
    }

    /**
     * @return the carbs
     */
    public String getCarbs() {
        return carbs;
    }

    /**
     * @param carbs the carbs to set
     */
    public void setCarbs(String carbs) {
        this.carbs = carbs;
    }

    /**
     * @return the protein
     */
    public String getProtein() {
        return protein;
    }

    /**
     * @param protein the protein to set
     */
    public void setProtein(String protein) {
        this.protein = protein;
    }

    /**
     * @return the cholesterol
     */
    public String getCholesterol() {
        return cholesterol;
    }

    /**
     * @param cholesterol the cholesterol to set
     */
    public void setCholesterol(String cholesterol) {
        this.cholesterol = cholesterol;
    }

    /**
     * @return the sodium
     */
    public String getSodium() {
        return sodium;
    }

    /**
     * @param sodium the sodium to set
     */
    public void setSodium(String sodium) {
        this.sodium = sodium;
    }
}
